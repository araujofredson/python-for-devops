# Condicionais são estruturas de controle de fluxo que permitem executar o código com base em condições. 
# as principais estruturas condicionais são if, elif e else.

a = 130
b = 130

if a > b:
    print(f"{a} é maior que {b}")
elif a == b:
    print(f"{a} é igual a {b}")
else:
    print(f"{a} é menor que {b}")