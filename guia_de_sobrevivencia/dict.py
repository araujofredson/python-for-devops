# dicts são mutáveis, ou seja, podem ser alterados depois de criados. 
# Eles são compostos por pares de chave-valor, onde cada chave é única e é usada para acessar o valor correspondente.
# Criando um dicionário
person_dict = {
    "name": "Matheus",  
    "age": 30,
    "city": "São Paulo"
}

# Acessando valores usando chaves
print(person_dict["name"])  # Output: Matheus
print(person_dict["age"])   # Output: 30
